public class E_Assignment_Operator {
    public static void main(String[] args) {
        int A = 10;
        // A = A + 10;
        A += 10;

        int B = 5;
        // B = B - 5;
        B -= 5;

        int C = 5;
        // C = C * 5;
        C *= 5;

        int D = 6;
        // D = D / 3;
        D /= 3;

        int E = 5;
        // E = E % 5;
        E %= 5;
    
        System.out.println(A);
        System.out.println(B);
        System.out.println(C);
        System.out.println(D);
        System.out.println(E);
    }
}
