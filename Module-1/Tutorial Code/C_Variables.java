public class C_Variables {
    public static void main(String[] args) {
        // storing integers number
        int a = 10;
        int b = 5;
        System.out.println(a);
        System.out.println(b);
        
        // storing a string
        String name = "Tony Stark";
        System.out.println(name);

        // changing variable value
        a = 50;
        System.out.println(a);
    }
}
